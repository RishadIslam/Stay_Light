package com.example.rishad.stay_light;

public class TitleImage {

    String id,url;

    public TitleImage () {}

    public TitleImage(String id, String url) {
        this.id = id;
        this.url = url;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
